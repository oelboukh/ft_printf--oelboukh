#include "ft_printf.h"

void	ft_putstr(char *s, int *len)
{
	int	i;

	i = 0;
	if (s == NULL)
	{
		write(1, "(null)", 6);
		*len = *len + 6;
	}
	else
	{
		while (s[i])
		{
			write(1, &s[i], 1);
			*len = *len + 1;
			i++;
		}
	}
}
